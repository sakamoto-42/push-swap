#include "push_swap.h"

void	reverse_rotate(t_stack **stack)
{
	t_stack	*prev_last;
	t_stack	*last;

	if (!stack || !(*stack) || !(*stack)->next)
		return ;
	last = *stack;
	while (last->next)
	{
		prev_last = last;
		last = last->next;
	}
	prev_last->next = NULL;
	last->next = *stack;
	*stack = last;
	ft_set_stack_position(*stack);
}
